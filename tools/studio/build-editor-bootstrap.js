#!/usr/bin/env node
/*
 * build-editor-bootstrap.js — derive the `editorBootstrap` Motoko Text constant
 * that apps/studio/src/Pages/Design.mview emits via @raw, from the canonical
 * editor source apps/studio/assets/mview-editor.js.
 *
 * MotoView has NO clean static-asset mechanism for shipping an arbitrary .js
 * file from a page (documented framework gap). The working path for a DEV TOOL
 * is to inline the editor as a <script type="module"> emitted via @raw.
 *
 * The editor module is base64-encoded and decoded+import()ed at runtime. base64
 * uses only [A-Za-z0-9+/=], so the editor's regexes / @( ) / braces / quotes
 * never reach the .mview or @code scanner — which would otherwise mis-parse a JS
 * regex like /^@(if|else)/ as a MotoView @( ) interpolation. Only the small,
 * author-controlled bootstrap boilerplate (which contains no such sequences) is
 * inlined as plain text.
 *
 *   node tools/studio/build-editor-bootstrap.js            # print constant
 *   node tools/studio/build-editor-bootstrap.js --inject   # write into Design.mview
 *   node tools/studio/build-editor-bootstrap.js --check     # exit 0 if up to date
 */
'use strict';

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..', '..');
const EDITOR_JS = path.join(REPO_ROOT, 'apps', 'studio', 'assets', 'mview-editor.js');
const DESIGN_PAGE = path.join(REPO_ROOT, 'apps', 'studio', 'src', 'Pages', 'Design.mview');

const BEGIN = '// === BEGIN editorBootstrap (generated) ===';
const END = '// === END editorBootstrap (generated) ===';

/** Escape a plain JS/HTML string into a Motoko string-literal body. */
function mo(s) {
  let out = '';
  for (const ch of s) {
    if (ch === '\\') out += '\\\\';
    else if (ch === '"') out += '\\"';
    else if (ch === '\n') out += '\\n';
    else if (ch === '\r') out += '';
    else if (ch === '\t') out += '\\t';
    else out += ch;
  }
  return out;
}

/**
 * The full inline bootstrap as ONE plain string (no Motoko escaping yet). It is
 * authored to contain no `@(`, no regex literals, and no constructs that could
 * confuse the @code / template scanner. The closing tag is split so it can never
 * terminate the host page early.
 */
function bootstrapText(b64) {
  // Chunk the base64 across JS string-concatenation lines so no single line is
  // gigantic in the generated Motoko.
  const lines = [];
  for (let i = 0; i < b64.length; i += 100) lines.push(b64.slice(i, i + 100));
  const jsPayload = lines.map((l) => '"' + l + '"').join(' +\n');

  const SCRIPT_OPEN = '<' + 'script type="module">';
  const SCRIPT_CLOSE = '<' + '/script>';

  return [
    SCRIPT_OPEN,
    'const __mvEditorB64 =',
    jsPayload + ';',
    'try {',
    '  const __mvbin = atob(__mvEditorB64);',
    '  const __mvbytes = new Uint8Array(__mvbin.length);',
    '  for (let __i = 0; __i < __mvbin.length; __i = __i + 1) { __mvbytes[__i] = __mvbin.charCodeAt(__i); }',
    '  const __mvblob = new Blob([__mvbytes], { type: "text/javascript" });',
    '  const __mvurl = URL.createObjectURL(__mvblob);',
    '  import(__mvurl).catch(function (e) {',
    '    const __s = document.getElementById("mv-editor-status");',
    '    if (__s) { __s.textContent = "editor module failed to load (CDN offline?) - showing source"; __s.dataset.kind = "warn"; }',
    '  });',
    '} catch (e) {}',
    SCRIPT_CLOSE
  ].join('\n');
}

function build() {
  const js = fs.readFileSync(EDITOR_JS, 'utf8');
  const b64 = Buffer.from(js, 'utf8').toString('base64');
  const text = bootstrapText(b64);

  // Escape the whole bootstrap into a Motoko literal, split per source-line so
  // the generated .mview is readable. Each line becomes "<escaped>\n" #, with the
  // last line carrying no trailing newline.
  const srcLines = text.split('\n');
  const motoko = srcLines
    .map((ln, idx) => {
      const nl = idx === srcLines.length - 1 ? '' : '\\n';
      return '"' + mo(ln) + nl + '"';
    })
    .join(' #\n');

  return [
    '  // Generated by tools/studio/build-editor-bootstrap.js from',
    '  // apps/studio/assets/mview-editor.js — DO NOT hand-edit; re-run:',
    '  //   node tools/studio/build-editor-bootstrap.js --inject',
    '  // The editor module is base64-inlined into a <script type="module"> emitted',
    '  // via @raw (MotoView has no clean app-static-asset mechanism — framework gap).',
    '  // base64 ([A-Za-z0-9+/=]) round-trips through the scanner with no escaping risk.',
    '  let editorBootstrap : Text =',
    motoko + ';',
    ''
  ].join('\n');
}

/** Inject (or replace) the generated constant between the markers in Design.mview. */
function inject() {
  const page = fs.readFileSync(DESIGN_PAGE, 'utf8');
  const bi = page.indexOf(BEGIN);
  const ei = page.indexOf(END);
  if (bi < 0 || ei < 0 || ei < bi) {
    throw new Error('Design.mview is missing the BEGIN/END editorBootstrap markers');
  }
  const before = page.slice(0, bi + BEGIN.length);
  const after = page.slice(ei);
  const next = `${before}\n${build()}${after}`;
  fs.writeFileSync(DESIGN_PAGE, next, 'utf8');
  return next;
}

/** Verify the page's generated block matches the current editor source. */
function check() {
  const page = fs.readFileSync(DESIGN_PAGE, 'utf8');
  const bi = page.indexOf(BEGIN);
  const ei = page.indexOf(END);
  if (bi < 0 || ei < 0) return false;
  const current = page.slice(bi + BEGIN.length, ei).trim();
  const expected = build().trim();
  return current === expected;
}

if (require.main === module) {
  if (process.argv.includes('--inject')) {
    inject();
    process.stdout.write('injected editorBootstrap into Design.mview\n');
  } else if (process.argv.includes('--check')) {
    process.exit(check() ? 0 : 1);
  } else {
    process.stdout.write(build());
  }
}

module.exports = { build, bootstrapText, inject, check, mo };
